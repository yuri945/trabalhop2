"""
Merge Sort
Vantagens:
- Complexidade garantida de O(n log n).
- Estável.
Desvantagens:
- Requer memória extra proporcional ao tamanho da lista.
"""

def merge_sort(arr):
    if len(arr) > 1:
        mid = len(arr) // 2
        L = arr[:mid]
        R = arr[mid:]
        merge_sort(L)
        merge_sort(R)
        i = j = k = 0
        while i < len(L) and j < len(R):
            if L[i] < R[j]:
                arr[k] = L[i]
                i += 1
            else:
                arr[k] = R[j]
                j += 1
            k += 1
        while i < len(L):
            arr[k] = L[i]
            i += 1
            k += 1
        while j < len(R):
            arr[k] = R[j]
            j += 1
            k += 1
    return arr

# Exemplo de uso
print(merge_sort([12, 11, 13, 5, 6, 7]))
